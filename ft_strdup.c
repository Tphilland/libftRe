#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int i;
	char *t;

	i = 0;
	t = (char *)malloc(sizeof(char));
	while (s1[i] != '\0')
	{
		t[i] = s1[i];
		i++;
	}
	return (t);
}
